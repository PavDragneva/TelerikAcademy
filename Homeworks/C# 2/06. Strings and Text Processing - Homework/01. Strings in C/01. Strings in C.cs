 * A string is a sequence of characters stored in a certain address in memory. 
 * Strings are very similar to the char arrays (char[]), but unlike them, they cannot be modified. 
 * Like the arrays, they have properties such as Length, which returns the length of the string and allows
 * access by index. Indexing, as it is used in arrays, takes indices from 0 to Length-1. 
 * Access to the character of a certain position in a string is done with the operator [] (indexer), 
 * but it is allowed only to read characters 
